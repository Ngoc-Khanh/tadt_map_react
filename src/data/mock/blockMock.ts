// HÃ m táº¡o block data hoÃ n chá»nh tá»« API response
export const createBlockFromApi = (
  apiResponse: IBlock | IBlock[]
): IBlockWithGeometry[] => {
  // Náº¿u apiResponse lÃ  array
  if (Array.isArray(apiResponse)) return addGeometryToBlocks(apiResponse);
  // Náº¿u apiResponse lÃ  single object
  return [getBlockWithGeometry(apiResponse)];
};

// HÃ m xá»­ lÃ½ SRO wrapper
export const processBlockSRO = (
  sroResponse: SRO<IBlock[]>
): IBlockWithGeometry[] => {
  if (!sroResponse.IsSuccess || !sroResponse.Data) {
    console.error("API Error:", sroResponse.ErrorMessage);
    return [];
  }
  return createBlockFromApi(sroResponse.Data);
};
